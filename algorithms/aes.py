def sub_word(word):
    return [SBOX[b] for b in word]

def rot_word(word):
    return word[1:] + word[:1]

def xor_words(a, b):
    return [x ^ y for x, y in zip(a, b)]
@app.route('/', methods=['GET','POST'])
def index():
    output = ""
    if request.method == 'POST':
        key_hex = request.form['key'].strip()
        rounds = int(request.form['rounds'])

        # Convert key to bytes
        key_bytes = [int(key_hex[i:i+2], 16) for i in range(0, 32, 2)]

        # Initial words W0–W3
        W = [key_bytes[i:i+4] for i in range(0, 16, 4)]

        output += "STEP 1: INITIAL WORDS (HEX)\n"
        output += "---------------------------------\n"
        for i in range(4):
            output += f"W{i} : "
            output += "  ".join(format(b, '02X') for b in W[i])
            output += "\n"

        # Key expansion rounds
        for r in range(rounds):
            output += f"\nROUND {r+1}\n"
            output += "---------------------------------\n"

            temp = W[-1]

            output += "RotWord : "
            rot = rot_word(temp)
            output += "  ".join(format(b,'02X') for b in rot) + "\n"

            sub = sub_word(rot)
            output += "SubWord : "
            output += "  ".join(format(b,'02X') for b in sub) + "\n"

            rcon = RCON[r]
            temp = xor_words(sub, rcon)

            output += "SubWord ⊕ Rcon : "
            output += "  ".join(format(b,'02X') for b in temp) + "\n\n"

            # Generate next 4 words
            W.append(xor_words(W[-4], temp))
            for i in range(1,4):
                W.append(xor_words(W[-4], W[-1]))

            for i in range(4):
                idx = len(W) - 4 + i
                output += f"W{idx} : "
                output += "  ".join(format(b,'02X') for b in W[idx])
                output += "\n"

    return render_template('AES.html', output=output)

